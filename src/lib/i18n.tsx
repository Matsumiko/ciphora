const dictionaries: Record<Locale, Record<TranslationKey, string>> = {
  id,
  en,
};

type TranslateOptions = Record<string, string | number>;

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, options?: TranslateOptions) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function normalizeLocale(value: string | null): Locale {
  return value === "en" || value === "id" ? value : defaultLocale;
}

function readInitialLocale(): Locale {
  if (typeof window === "undefined") return defaultLocale;
  return normalizeLocale(readStorageValue(window.localStorage, STORAGE_KEYS.language));
}

function interpolate(template: string, options?: TranslateOptions) {
  if (!options) return template;

  return Object.entries(options).reduce(
    (result, [key, value]) => result.replaceAll(`{{${key}}}`, String(value)),
    template,
  );
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(readInitialLocale);

  useEffect(() => {
    if (typeof window !== "undefined") {
      writeStorageValue(window.localStorage, STORAGE_KEYS.language, locale);
    }
    document.documentElement.lang = locale === "id" ? "id" : "en";
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: TranslationKey, options?: TranslateOptions) => {
      const template = dictionaries[locale][key] ?? dictionaries[defaultLocale][key] ?? key;
      return interpolate(template, options);
    };

    return {
      locale,
      setLocale: setLocaleState,
      t,
    };
  }, [locale]);

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}
